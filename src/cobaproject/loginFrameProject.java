    public void submit(){
        try{
            sql="SELECT * FROM admin WHERE username='"+usern.getText()+"' AND password='"+passw.getText()+"'";
            rs = stat.executeQuery(sql);
        
            if(rs.next()){
                if(usern.getText().equals(rs.getString("username")) && passw.getText().equals(rs.getString("password"))){
                    berandaFrameMaster bf = new berandaFrameMaster();
                    bf.setId(rs.getString("id"));
                    bf.setVisible(true);
                    dispose();
                }
            }else{
                sql="SELECT * FROM petugas WHERE username='"+usern.getText()+"' AND password='"+passw.getText()+"'";
                rs = stat.executeQuery(sql);
                if(rs.next()){
                    if(usern.getText().equals(rs.getString("username")) && passw.getText().equals(rs.getString("password"))){
                        berandaFrameMaster bf = new berandaFrameMaster();
                        bf.setId(rs.getString("id"));
                        bf.setVisible(true);
                        dispose();
                    }
                }else{
                    sql="SELECT * FROM anggota WHERE username='"+usern.getText()+"' AND password='"+passw.getText()+"'";
                    rs = stat.executeQuery(sql);
                    if(rs.next()){
                        if(usern.getText().equals(rs.getString("username")) && passw.getText().equals(rs.getString("password"))){
                            berandaFrameUser bf = new berandaFrameUser();
                            bf.setId(rs.getString("id"));
                            bf.setVisible(true);
                            dispose();
                        }
                    }else{
                        JOptionPane.showMessageDialog(null,"username atau password tidak valid");
                    }
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
