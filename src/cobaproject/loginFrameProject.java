    private void passwKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passwKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            submit();
        }
        if(evt.getKeyCode() == KeyEvent.VK_UP){
            usern.requestFocus();
        }
        if(evt.getKeyCode() == KeyEvent.VK_DOWN){
            showpass.requestFocus();
        }
    }//GEN-LAST:event_passwKeyPressed

    private void showpassKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_showpassKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(showpass.isSelected()){
                showpass.setSelected(false);
                passw.setEchoChar('*');
                submit.requestFocus();
            }else{
                showpass.setSelected(true);
                passw.setEchoChar((char)0);
                submit.requestFocus();
            }
        }
        if(evt.getKeyCode() == KeyEvent.VK_UP){
            passw.requestFocus();
        }
        if(evt.getKeyCode() == KeyEvent.VK_DOWN){
            submit.requestFocus();
        }
    }//GEN-LAST:event_showpassKeyPressed

    private void submitKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_submitKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            submit();
        }
        if(evt.getKeyCode() == KeyEvent.VK_UP){
            showpass.requestFocus();
        }
        if(evt.getKeyCode() == KeyEvent.VK_DOWN){
            register.requestFocus();
            register.setForeground(Color.blue);
        }
    }//GEN-LAST:event_submitKeyPressed

    private void registerKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_registerKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            formAnggotaUser fr = new formAnggotaUser();
            fr.setVisible(true);
            dispose();
        }
        if(evt.getKeyCode() == KeyEvent.VK_UP){
            submit.requestFocus();
            register.setForeground(Color.black);
        }
        if(evt.getKeyCode() == KeyEvent.VK_DOWN){
            keluar.requestFocus();
            register.setForeground(Color.black);
        }
    }//GEN-LAST:event_registerKeyPressed

    private void keluarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_keluarKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            int selectOpt= JOptionPane.showConfirmDialog(null, "Anda yakin?","Tutup aplikasi",JOptionPane.YES_NO_OPTION);
        
            if(selectOpt == JOptionPane.YES_OPTION){
                System.exit(0);
            }
        }
        if(evt.getKeyCode() == KeyEvent.VK_UP){
            register.requestFocus();
            register.setForeground(Color.blue);
        }
    }//GEN-LAST:event_keluarKeyPressed
    
